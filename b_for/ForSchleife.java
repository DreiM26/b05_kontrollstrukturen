public class ForSchleife
{
   public static void main(String[] args)
   {
         // Wann ; Bedingungen ; "Formula" i++ = i=i+1
         // Buscar composici√≥n de for()
         // Der Kopf der for-Schleife eteuert die
         // Anzahl der Wiederholungen.

         // Er hat drei Abschnitte:
         //
         // 1.) Deklaration der Zaehlvariablen i
         // und Initialisierung mit den STARTWERT 1.
         // 2.) Bedingung fuer den ENDWERT: i darf nur
         // kleiner oder gleich nach 20 sein (nicht groesser)

      for(int i = 1;i <= 20; i++)
      {
         System.out.println("Hallo " + i);
      }
   
      
   }
   
}
